def parse_cycles(s):
    s = s.strip()
    m = re.match(r'^([\d.]+)([TGMK]?)$', s)
    if not m:
        return 0
    val = float(m.group(1))
    suffix = m.group(2)
    multipliers = {'T': 1e12, 'G': 1e9, 'M': 1e6, 'K': 1e3, '': 1}
    return val * multipliers.get(suffix, 1)

def parse_rewards(s):
    s = s.strip()
    # Remove " ZKC" suffix
    s = re.sub(r'\s*ZKC\s*$', '', s).strip()
    m = re.match(r'^([\d,.]+)([KMT]?)$', s)
    if not m:
        return 0
    val_str = m.group(1).replace(',', '')
    val = float(val_str)
    suffix = m.group(2)
    multipliers = {'T': 1e12, 'M': 1e6, 'K': 1e3, '': 1}
    return val * multipliers.get(suffix, 1)

